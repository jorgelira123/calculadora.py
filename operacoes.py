import math


def soma(a, b):

    return a + b


def subtracao(a, b):

    return a - b


def multiplicacao(a, b):

    return a * b


def divisao(a, b):

    return a / b


def potencia(a, b):

    return a ** b


def raiz_quadrada(a):

    return math.sqrt(a)


def logaritmo(a):

    return math.log(a)